public class for_loops{
    public static void main(String[] args) {
    // i += 1 also works
        for(int i = 0; i < 5; ++i ){
            System.out.printf("The value of i is %d \n", i);
            //System.out.println(); --> This can also be a new line
            // We can use printf, for formatting purposes instead of concatenation
            // %d signifies an integer value
        }
        
    }
}   